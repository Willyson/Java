CREATE DATABASE VENDAS; 

USE VENDAS;
 

CREATE TABLE USUARIO
(
    ID_USUARIO INT NOT NULL AUTO_INCREMENT,
    NOME_USUARIO VARCHAR(50), 
    SENHA_USUARIO VARCHAR(50),
    STATUS_USUARIO CHAR(1),

    PRIMARY KEY(ID_USUARIO)

);
